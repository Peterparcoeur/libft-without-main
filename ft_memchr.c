#include "libft.h"

void		*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (str[i] && i < n)
	{
		if (str[i] == c)
		{
			str[i] = (unsigned char)str[i];
			return (&str[i]);
		}
		i++;
	}
	return (str);
}
