#include "libft.h"

char		*ft_strchr(char *s, int c)
{
	int		i;
	char	x;

	i = 0;
	x = (char)c;
	while (*s != '\0')
	{
		while (*s != x)
		{
			s++;
			if (*s == '\0')
				return (NULL);
			else if (*s != '\0' && *s == x)
				return (s);
		}
	}
	return (NULL);
}
